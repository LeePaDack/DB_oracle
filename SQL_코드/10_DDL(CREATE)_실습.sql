-- SNACKS 테이블 생성
CREATE TABLE SNACKS(
    SNACK_ID NUMBER(10,0) PRIMARY KEY, -- 과자ID (키본키)
    SNACK_NAME VARCHAR2(100) NOT NULL, -- 과자 이름 (NULL 불가)
    PRICE NUMBER(10,2) NOT NULL, -- 가격 (NULL 불가)
    COMPANY_ID NUMBER(10,0), -- 핵심포인트
    CONSTRAINT FK_COMPANY FOREIGN KEY(COMPANY_ID)
    REFERENCES FOOD_COMPANY(COMPANY_ID) ON DELETE CASCADE
    -- FOOD_COMPANY 회사 ID ON DELETE CASCADE -- 부모 삭제시 자식까지 삭제해줘
);
